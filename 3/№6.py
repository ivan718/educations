age = 21;

if age <= 1:
    print("Новорождённый")
elif 1 < age and age <= 10:
    print("Ребёнок")
elif 10 < age and age <= 16:
    print("Подросток")
elif 16 < age and age <= 29:
    print("Типо молодой")
elif 29 < age and age <= 59:
    print("Уже взрослый получается")
else:
    print("Старый уже стал")
